    private void buscar() {
        String ccString = jTextFieldCC.getText();
        int cc = Integer.parseInt(ccString);
        System.out.println(principal.listaCircularCliente.getCantidad());
        Cliente actual = principal.listaCircularCliente.buscar(cc);
        if (actual != null) {
            System.out.println("si esta");
        } else {
            System.out.println("no esta");
        }
        llenarCampos(actual);
    }

    private void llenarCampos(Cliente actual) {
        String ccString = jTextFieldCC.getText();
        int cc = Integer.parseInt(ccString);
        jTextFieldNombre.setText(principal.getListaCircularCliente().buscar(cc).getNombreCliente());
        jTextFieldApellido.setText(principal.getListaCircularCliente().buscar(cc).getApellidoCliente());
        jTextFieldNacionalidad.setText(principal.getListaCircularCliente().buscar(cc).getNacionalidadCliente());
        jTextFieldCorreo.setText(principal.getListaCircularCliente().buscar(cc).getCorreoCliente());
        int telefonoInt = principal.getListaCircularCliente().buscar(cc).getTelefonoCliente();
        String telefonoString = Integer.toString(telefonoInt);
        jTextFieldTelefono.setText(telefonoString);
    }

    private void buscarParaCC() {
        String ccString = jTextFieldCC.getText();
        int cc = Integer.parseInt(ccString);
        System.out.println(principal.listaCircularCliente.getCantidad());
        Cliente actual = principal.listaCircularCliente.buscar(cc);
        if (actual != null) {
            System.out.println("si esta");
        } else {
            System.out.println("no esta");
        }
        llenarCampos(actual);
    }

    private void borrar() {
        String ccString = jTextFieldCC.getText();
        int cc = Integer.parseInt(ccString);
        principal.listaCircularCliente.eliminarPorDato(cc);
        actualizarListaCliente();;
        principal.volverAGestionClientePresidente(this);
    }

    private void actualizarListaAviones() {
        String[] aviones = principal.controladorPilaAvion.darAviones();
    }

    private void agregar() {
        try {
            String nombre = jTextFieldNombre.getText();
            String apellido = jTextFieldApellido.getText();
            String ccString = jTextFieldCC.getText();
            int cc = Integer.parseInt(ccString);
            String generoString = jComboBoxGenero.getActionCommand();
            char genero = generoString.charAt(1);
            String nacionalidad = jTextFieldNacionalidad.getText();
            String correo = jTextFieldCorreo.getText();
            String telefonoString = jTextFieldCC.getText();
            int telefono = Integer.parseInt(telefonoString);
            String visaString = jComboBoxVisa.getActionCommand();
            boolean visa = false;
            if (visaString == "Si lo tiene") {
                visa = true;
            } else {
                visa = false;
            }
            Cliente actual = new Cliente(nombre, apellido, cc, genero, nacionalidad, correo, telefono, visa);
            boolean valor = principal.listaCircularCliente.agregarCabeza(actual);
            System.out.println(actual);
            if (valor == false) {
                JOptionPane.showMessageDialog(this, "El cliente ya existe", "Error al agregar el nuevo cliente", JOptionPane.INFORMATION_MESSAGE);
            } else {
                principal.listaCircularCliente.agregarCabeza(actual);
                principal.volverAGestionClientePresidente(this);
            }
            System.out.println("cuantos" + principal.listaCircularCliente.getCantidad());
        } catch (java.lang.NumberFormatException e2) {
            JOptionPane.showMessageDialog(this, "Tiene un campo donde el valor no es valido o esta vacio", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void actualizarListaCliente() {
        String cliente = principal.listaCircularCliente.mostrarLista();
    }

