    private void jButtonEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarClienteActionPerformed
        try {
            borrar();
        } catch (java.lang.NullPointerException e1) {
            Icon imagenError = new ImageIcon(getClass().getResource("/iconos/baseline_warning_black_36dp.png"));
            JOptionPane.showMessageDialog(this, "El cliente no existe", "Error al eliminar al cliente", JOptionPane.INFORMATION_MESSAGE, imagenError);
        } catch (java.lang.ArrayIndexOutOfBoundsException e2) {
            Icon imagenError = new ImageIcon(getClass().getResource("/iconos/baseline_warning_black_36dp.png"));
            JOptionPane.showMessageDialog(this, "El cliente no existe", "Error al eliminar al cliente", JOptionPane.INFORMATION_MESSAGE, imagenError);
        } catch (java.lang.NegativeArraySizeException e3) {
            borrar();
        }
    }//GEN-LAST:event_jButtonEliminarClienteActionPerformed

