    private void borrar() {
        String ccString = jTextFieldCC.getText();
        int cc = Integer.parseInt(ccString);
        principal.listaCircularCliente.eliminarPorDato(cc);
        principal.volverAGestionClientePresidente(this);
        volver();
    }

    private void volver() {
        principal.volverAGestionClienteDirector(this);
    }

