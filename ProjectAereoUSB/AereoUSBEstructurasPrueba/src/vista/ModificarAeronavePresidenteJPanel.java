    private void llenarCampos(Avion aviones) {
        String matricula = jTextFieldMatricula.getText();
        jTextFieldMarca.setText(principal.getControladorPilaAvion().buscarPorMatricula(matricula).getMarca());
        jTextFieldModelo.setText(principal.getControladorPilaAvion().buscarPorMatricula(matricula).getModelo());
        int cantidadAsientosINT = principal.getControladorPilaAvion().buscarPorMatricula(matricula).getNumeroAsientos();
        String cantidadAsientosString = Integer.toString(cantidadAsientosINT);
        jTextFieldCantidadAsientos.setText(cantidadAsientosString);
        Double cantidadEquipaje = principal.getControladorPilaAvion().buscarPorMatricula(matricula).getCantidadEquipajeSoportado();
        String cantidadEquipajeString = String.valueOf(cantidadEquipaje);
        jTextFieldCantidadEquipaje.setText(cantidadEquipajeString);
        Double costoMantenimiento = principal.getControladorPilaAvion().buscarPorMatricula(matricula).getCostoMantenimiento();
        String costoMantenimientoString = String.valueOf(costoMantenimiento);
        jTextFieldCostoMantenimiento.setText(costoMantenimientoString);
        Double costoHangar = principal.getControladorPilaAvion().buscarPorMatricula(matricula).getCostoHangar();
        String costoHangarString = String.valueOf(costoHangar);
        jTextFieldCostoHangar.setText(costoHangarString);
    }

    private void buscarParaModificar() {
        String matricula = jTextFieldMatricula.getText();
        Avion actual = principal.controladorPilaAvion.buscarPorMatricula(matricula);
        llenarCampos(actual);
    }

    private void borrar() {
        String matricula = jTextFieldMatricula.getText();
        principal.controladorPilaAvion.popAvion(matricula);
        System.out.println("Borra campos");
        System.out.println("");
        actualizarListaAviones();;
        System.out.println(principal.controladorPilaAvion.TotalAviones());
    }

    private void actualizarListaAviones() {
        String[] aviones = principal.controladorPilaAvion.darAviones();
    }

    private void agregar() {
        try {
            String nMatricula = jTextFieldMatricula.getText();
            String marca = jTextFieldMarca.getText();
            String modelo = jTextFieldModelo.getText();
            String cantidadAsientosString = jTextFieldCantidadAsientos.getText();
            int numeroAsientos = Integer.parseInt(cantidadAsientosString);
            String cantidadEquipajeString = jTextFieldCantidadEquipaje.getText();
            double cantidadEquipajeSoportado = Double.parseDouble(cantidadEquipajeString);
            String costoMantenimientoString = jTextFieldCostoMantenimiento.getText();
            double costoMantenimiento = Double.parseDouble(costoMantenimientoString);
            String costoHangarString = jTextFieldCostoHangar.getText();
            double costoHangar = Double.parseDouble(costoHangarString);
            Avion actual = new Avion(nMatricula, marca, modelo, numeroAsientos, cantidadEquipajeSoportado, costoMantenimiento, costoHangar);
            System.out.println(actual);
            boolean valor = principal.controladorPilaAvion.pushAvion(actual);
            System.out.println("cuantos" + principal.controladorPilaAvion.TotalAviones());
            if (valor == false) {
                JOptionPane.showMessageDialog(this, "El avion ya existe", "Agregar", JOptionPane.INFORMATION_MESSAGE);
            } else if (valor == true) {
                principal.controladorPilaAvion.pushAvion(actual);
                principal.volverAGestionAeronavePresidente(this);
            }
        } catch (java.lang.NumberFormatException e2) {
            JOptionPane.showMessageDialog(this, "Tiene un campo donde el valor no es valido o esta vacio", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
